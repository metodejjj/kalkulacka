
class kalkulacka:
    def secti (self,a,b):
        """

        :param a: vstup libovolné číslo
        :param b: vstup libovolné číslo

        :return: součet
        """
        return a+b


    def odecti (self,a,b):
        """

        :param a: vstup libovolné číslo
        :param b: vstup libovolné číslo
        :return: rozdíl
        """
        return a-b

    def vynásob (self,a,b):
        """

        :param a: vstup libovolné číslo
        :param b: vstup libovolné číslo
        :return: násobek
        """
        return a*b

    def vyděl (self,a,b):
        """

        :param a: vstup libovolné číslo
        :param b: vstup libovolné číslo
        :return: dělení
        """

        return a/b

